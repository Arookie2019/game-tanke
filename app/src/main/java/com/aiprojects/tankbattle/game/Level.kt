package com.aiprojects.tankbattle.game

/**
 * 单层关卡数据：ASCII 地图与敌军总数。
 *
 * 字符约定：`#` 砖、`@` 钢、`P` 玩家、`E` 敌军出生点、`B` 基地、`.` 空地。
 */
data class Level(
    val title: String,
    val lines: List<String>,
    val enemiesInLevel: Int,
) {
    init {
        require(lines.isNotEmpty()) { "关卡不能为空" }
        val width = lines.first().length
        require(lines.all { it.length == width }) { "关卡每行宽度必须一致" }
        require(enemiesInLevel > 0) { "敌军数量必须大于 0" }
    }

    val cols: Int get() = lines.first().length
    val rows: Int get() = lines.size
}

