/**
 * 生成 04–12 关 assets 文本（宽度 W=21）。
 * 运行：node tools/gen_extra_levels.mjs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUT = path.join(__dirname, "..", "app", "src", "main", "assets", "levels");

const W = 21;

function border() {
  return "#".repeat(W);
}

function row(inner) {
  if (inner.length !== W - 2) {
    throw new Error(`inner len ${inner.length} (need ${W - 2}): ${inner}`);
  }
  return `#${inner}#`;
}

const DOT19 = ".".repeat(19);
const DOT18 = ".".repeat(18);

function writeLevel(filename, title, enemies, rows) {
  const body = rows.join("\n") + "\n";
  for (const line of rows) {
    if (line.length !== W) throw new Error(`${filename}: bad row len ${line.length}: ${line}`);
  }
  const text = `@TITLE ${title}\n@ENEMIES ${enemies}\n${body}`;
  fs.writeFileSync(path.join(OUT, filename), text, "utf8");
  console.log("wrote", filename);
}

